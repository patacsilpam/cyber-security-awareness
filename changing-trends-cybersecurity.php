                        <h1>The Changing Trends In Cyber Security</h1>
                    </div>
                    <div>
                        <p class="texts">According to figures released in 2020 by the Department for Digital, Culture, Media and Sport, the UK’s cyber security industry is worth £8.3bn. However, the picture isn’t as cohesive as you might assume – 
                        different areas of the cyber security industry are growing at varying rates and world events continue to shape developing trends.</p>
                    </div>
                    <img src="https://www.cisltd.com/app/uploads/2020/09/AdobeStock_191967596-1024x512.jpeg" class="img">
                    <div>
                        <p class="texts">We’ve seen, and will continue to see, UK businesses digitising their processes, including the transfer, storage and processing of important data and communication. 
                        But we’re also seeing more frequent and sophisticated cyber-attacks on enterprise systems. The UK IT security sector is continuing to reshape itself to meet these evolving threats and the changing demands of organisations.</p>
                        <p class="texts">Here are five trends to watch in this space:</p>
                        <h2 class="texts h2-title">Trend 1: Proactively adapting to evolving threats</h2>
                        <p class="texts">The threat and regulatory environment is evolving rapidly, and organisations are hurrying to adapt to these changes. 
                        The pandemic has produced changes in attack methods at the same time regulation has become more complicated and potentially more costly. 
                        Security providers, and their customers, have to balance these considerations and develop their solutions to meet this changing environment.</p>
                        <p class="texts">Managed security services are becoming more proactive, offering both offensive and defensive capabilities. 
                        We’re seeing more customers looking for providers who will both coordinate their incident response team and carry out the more day-to-day tasks of managed detection and response. We expect to see more cyber security and fusion centres 
                        created to expand security operations, using emerging technologies like AI and machine learning to deliver layered security and provide insights.</p>
                    </div>
                    <div>
                        <h2 class="texts h2-title">Trend 2: A focus on user-friendliness</h2>
                        <p class="texts">As a result of these changing threats (and more stringent regulations), enterprises will focus on working with tools and 
                        services that are easy to understand and use. At the moment, it’s especially important that security service providers offer solutions that 
                        can cover both legacy and digital systems in a relatively uncomplicated way. Data loss prevention providers are one of the 
                        specialists seeing high demand for ease-of-use solutions from their clients.</p>
                    </div>
