                        <h1>Facebook tests a new security feature for Instagram that will allow users</h1>
                    </div>
                    <div>
                        <p class="texts">The Facebook-owned app, Instagram is one of the biggest social network with over a billion users across the world. It is working on varieties of features for the convenience of its users as a few weeks ago, it was 
                        working on a feature to upload photos or videos through a web browser just like people upload on Facebook through a desktop device. </p>
                        <p class="texts">Recently, Facebook was spotted testing a feature on Instagram that will allow users to receive two-factor verification codes through WhatsApp messenger or WhatsApp business as the latest option. Instagram has stated 
                        that it will not be a mandatory feature that will bound the users to use WhatsApp for verification codes, if users have privacy issue and they do not feel safe to receive 2FA (two-factor authentication) codes through WhatsApp, they 
                        are allowed to use app authentication to generate 2FA codes. Instagram already uses multiple options to send verification codes to its users like text messages, authenticator apps, and now it is time for WhatsApp to join these options.</p>
                    </div>
                    <img src="https://1.bp.blogspot.com/-7JFx7Eb-vVM/YKsvsReFihI/AAAAAAAAZVE/D3CMXe8H8zsextZQRVBLMejhr3Kw47QigCLcBGAsYHQ/s16000/fb-ig-wa-2fa.png" class="img">
                    <div>
                        <p class="texts">App researcher and reverse engineer named Alessandro Paluzzi tweeted screenshots of the feature and said that it is still in the development stage but it will be launched soon both on Android and iOS. The screenshots posted 
                        by Alessandro have shown that there will be a toggle option to turn on the verification codes on the WhatsApp number, however, if users want to turn on this option, they will have to enable the text message option before. For those users, who 
                        want to know that how they can turn this option on, they can turn this option on by going to Instagram settings, then security, and they will find the option of 2FA, they can switch on the WhatsApp option and if they do not feel comfortable using 
                        this option, they can even turn this option off.</p>
                        <p class="texts">2FA increases the security of your account and it is very important because hackers use different techniques to get access to your account and in 2FA, you give two types of info such as a secret password or you enter a login code 
                        that you receive on your device for one time. Users should keep in mind one thing that not all the options are equally safe in 2FA, as the text message is the least safe option to receive the two-factor authentication codes because messages can be
                        leaked easily. Some people may think that through WhatsApp they will still get the code in the form of a text message; therefore, Instagram will set this as optional.</p>
                        <p class="texts">People can use an authentication app or Google authenticator as a safer way but they will have to depend on a secret key and sometimes that can become a flaw. Experts say that the most secure way to receive the 2FA is through U2F 
                        (Universal second-factor keys), however, each user opt different option.</p>
