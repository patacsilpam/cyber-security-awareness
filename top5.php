                        <h1 >Top 5 Ways to Protect Your Digital Identify Online</h1>
                    </div>
                    <div>
                        <p class="texts">The security dangers increase as the percentage of the population who uses the internet grows.</p>
                        <p class="texts">Cyberattacks have become so common that the US federal government has set up a research and development agency 
                        to come up with a strategy for developing technology and establishing rules to reduce the hazards in cyberspace.</p>
                        <p class="texts">People are exposing more personal information to the internet world as they use social media sites and networks, making it increasingly difficult to preserve valuable data. 
                        Thankfully, there are several methods for safeguarding your online identity and personal data.</p>
                    </div>
                    <img src="https://i.pcmag.com/imagery/articles/01zU35AJIGBtH8jr1uw11Qd-12.1616112217.fit_lim.jpg" class="img">
                    <div>
                        <p class="texts">Here are the top ten strategies to keep yourself safe online:</p>
                        <h2 class="texts h2-title">1. Make Use of Secure Passwords</h2>
                        <p class="texts">Focusing on setting strong passwords is the most popular strategy to protect your online identity. Choose a password that will not be easily cracked or decoded when creating one. Don't use a first, middle, or last name, a spouse's or child's name,
                         address, phone numbers, employers, or other identifying letters or numbers that can be associated with you.</p>  
                    </div>
                    <div>
                        <h2 class="texts h2-title">2. Activate the Web Browser Blacklisting feature.</h2>
                        <p class="texts">Part of the reason for the lack of internet security is the browser being used. 
                        Additional security features, such as blacklisting, are available in many online browsers. This enables you to specify the criteria for which sites you will access; only secure, trusted sites will be available for you to access.</p>
                    </div>
                    <div>
                        <h2 class="texts h2-title">3. Avoid Phishing Scams</h2>
                        <p class="texts">Phishing schemes utilize a range of techniques to steal your personal information and identity. There are many different types of phishing scams, but you can avoid them by learning how to spot them. To prevent being a victim of a phishing scam,
                         never open unknown sender emails or attachments, and never click on unsafe links in unusual emails.</p>
                    </div>
                    <div>
                        <h2 class="texts h2-title">4. Only allow cookies in your web browser when absolutely necessary.</h2>
                        <p class="texts">Another way to protect your online data is to set up your browser to only allow cookies when they are necessary by a website. Cookies are small pieces of information that websites save on your computer, such as which websites you visit and what you do there. The majority of them keep the details to themselves, 
                        but this is also a way for dishonest persons to obtain your personal information.</p>
                    </div>
                    <div>
                        <h2 class="texts h2-title">5. Protect Your Personal Data</h2>
                        <p class="texts">If you don't set up your web browser properly, it's likely that you'll unwittingly reveal personal information with others. You can configure a new computer or download and install a new browser if you obtain a new one. To do so, go to the browser's "set-up" menu and
                         select "don't reveal your name, email address, or other information" from the drop-down menu.</p>
                    </div>
