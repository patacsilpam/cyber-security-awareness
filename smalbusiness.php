                        <br>
                        <h1>Small Business Cybersecurity 101: Simple Tips To Protect Your Data</h1>
                    </div>
                    <div>
                        <p class="texts">Cybercriminals aren’t only going after big corporations. For every Equifax or Colonial Pipeline you read about in the news, 
                        there are thousands of small businesses that have had their sensitive company or customer information compromised. </p>
                        
                    </div>
                    <img src="https://specials-images.forbesimg.com/imageserve/5f85e2d311efbeb805bfbaca/960x0.jpg?cropX1=3446&cropX2=10000&cropY1=0&cropY2=4000" class="img">
                    <div>
                        <h2 class="texts h2-title">Why Is Cybersecurity A Concern For Small Businesses?</h2>
                        <p class="texts">According to the <a href="https://enterprise.verizon.com/resources/reports/2020-data-breach-investigations-report.pdf" target="0">Verizon Data Breach Investigations Report</a> for 2020, small businesses accounted for 28% of data breach victims.
                        Because they aren't secured by the same amount of security architecture as larger enterprises, smaller businesses are typically excellent targets for cyberattacks. However, they still have significant data to give.
                        </p>  
                    </div>
                    <div>
                        <p class="texts">According to a poll conducted by the <a href="https://staysafeonline.org/small-business-target-survey-data/" target="0">National Cyber Security Alliance (NCSA)</a>, 28% of small business respondents had experienced an official data breach in the previous 12 months.
                        Following a data breach, 37% of organizations incurred financial losses, 25% filed for bankruptcy, and 10% went out of business.The majority of small business decision makers (88%) believe they are at least a fairly likely target for cyberattacks, and 63 percent say
                        cybersecurity is a top priority for their company. However, only 46% felt well prepared to respond promptly in the event of a data breach.
                        </p>
                    </div>
                    <div>
                        <p class="texts">For your company, untangling the consequences of a cyberattack can be costly, frustrating, and time-consuming.
                        However, you may take easy, proactive efforts to protect your company's most sensitive data.</p>
                    </div>
                    <div>
                        <h2 class="texts h2-title">Identify Your Vulnerabilities </h2>
                        <p class="texts">Work with your internal IT staff or an external security consultant to audit your current cybersecurity practices and protections.
                         These are common areas of vulnerability for small businesses: </p>
                        <p class="texts"><b>• Underfunded or inadequate IT staff:</b></p>
                        <p class="texts">In a small business, cybersecurity is often an afterthought. You may usually outsource cybersecurity monitoring and 
                        protection for a lower monthly cost if you don't have the budget for a dedicated IT crew.</p>
                        <p class="texts"><b>• Insufficient computer and network security:</b></p>
                        <p class="texts">Hackers can gain access to company systems in a number of ways, 
                        including unsecured Wi-Fi networks or personal devices, weak passwords or software with outdated security patches. </p>    
                    </div>
                    <div>
                        <h2 class="texts h2-title">Create A Cybersecurity Plan </h2>
                        <p class="texts">Once you have assessed your current risks, you can develop new procedures to minimize your exposure to cyberattacks.  </p>
                        <p class="texts"><b>• Establish regular assessments:</b></p>
                        <p class="texts">Install vulnerability scanning software to keep an eye on your networks, websites, software, and other systems,
                        as well as firewalls, VPNs, and antivirus software.</p>  
                        <p class="texts"><b>• Keep software updated:</b></p>
                        <p class="texts">Always run the latest versions of software, installing patches as soon as they’re released. </p>         
                        <p class="texts"><b>• Backup data:</b></p>  
                        <p class="texts">Backup all data on-site and with a cloud provider to protect your firm from ransomware threats.</p> 
                        <p class="texts"><b>• Have a response strategy:</b></p>
                        <p class="texts">Prepare for the worst-case situation and establish clear protocols for dealing with a cyberattack.
                        Establish a reaction team and conduct practice runs to improve protocols.</p>
                    </div>
                    <div>
                        <h2 class="texts h2-title">Educate Your Employees </h2>
                        <p class="texts">Stronger cybersecurity starts with better employee education. Provide regular training for your team, teaching them about common threats and outlining company security requirements.</p>
                        <p class="texts"><b>• Build security into devices:</b></p>
                        <p class="texts">Know which devices your employees use to access company data, such as laptops, phones, and tablets, especially if they work remotely. 
                        Extend security protocols to all work-related devices, such as data encryption, password management, and software updates.</p>
                        <p class="texts"><b>• Recognize common scams: </b></p>
                        <p class="texts">Educate employees on how to recognize common cybercriminal strategies such as phishing emails or texts,
                        malware downloads, social media spam, and social engineering attempts.</p>
                        <p class="texts">A cyberattack can have huge consequences for your company. You can save time, money, and misery in the future by taking an active part in your cybersecurity and implementing precautionary steps now.</p>
                    </div>
