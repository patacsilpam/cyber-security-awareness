                        <h1>Google Workspace, Client-Side Encryption is now supported</h1>
                    </div>
                    <div>
                        <p class="texts" style="color: #3c3c3c;">Google has announced that users will have control over encryption keys.</p>
                    </div>
                    <img src="https://storage.googleapis.com/gweb-cloudblog-publish/images/Set_granular_sharing_rules.max-2800x2800.jpg" class="img">
                    <div>
                        <p class="texts">Google Workspace applications - formerly known as G Suite -
                         Google now supports client-side encryption, which allows users to manage their own encryption keys.</p>
                        <p class="texts">"User data is impossible to decipher to Google with client-side encryption," the company noted in a blog post unveiling the new feature today.
                          "Users can continue to utilize Google's native web-based collaboration, view content on mobile devices, and exchange encrypted files externally."</p>
                        <p class="texts">According to Google, client side encryption is intended in part at regulated industries that keep sensitive data, such as healthcare records and financial data.</p>
                        <p class="texts">Andrew Plunkett, Airbus's head of Digital Workplace, said his company now employs the capability for its Google Workspace apps in a statement included with the launch.</p>
                        <p class="texts">"Client-side encryption is a cornerstone of our modern information governance strategy," he said.</p>
                        <p class="texts">Click <a href="https://cloud.google.com/blog/products/workspace/new-google-workspace-security-features" target="0">here</a> to read Google's announcement.</p>
