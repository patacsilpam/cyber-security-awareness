                        <h1>Detecting and Preventing Cyberattacks with Breached Password Detection Services</h1>
                    </div>
                    <div>
                        <p class="texts">A strong cybersecurity strategy includes enhancing password policies in your firm. One of cybercriminals' 
                        preferred strategies for infiltrating business-critical environments is utilizing hacked accounts; as we've seen in recent
                        news, these attacks can be risky and financially devastating. </p>
                        
                    </div>
                    <img src="https://thehackernews.com/images/-BZHWSbxVJoo/YMD4DtB--_I/AAAAAAAABC8/nQNhj86czWgTZ0HYuy6-rVl_HsTaoqVygCLcBGAsYHQ/s728-e1000/data-breach-protection.jpg" class="img">
                    <div>
                        <p class="texts">However, account breach is a highly effective attack vector that needs far less effort than other attack vectors.
                        Breached password detection is one of the most important methods of password protection specified by well-known cybersecurity standards.
                        In credential stuffing and password spraying cyberattacks, hackers frequently leverage known hacked password lists.</p>
                        <p class="texts">When your sysadmins are exploring into broken password protection solutions, there are a few things to keep in mind.</p>
                    </div>
                    <div>
                        <h2 class="texts h2-title"><b>Breached password recommendations</b></h2>
                        <p class="texts">In recent years, password security advice have progressed beyond the typical password security advice.
                        Businesses have been using Microsoft Active Directory to implement password policies for years. Minimum password 
                        configuration options are included in standard Active Directory password policies.</p>
                        <p class="texts">An example of the options available with a standard Active Directory Password Policy can be found below:</p>
                    </div>
                    <div>
                        <ul class="lists">
                            <li>Enforce password history</li>
                            <li>Maximum password age</li>
                            <li>Minimum password age</li>
                            <li>Minimum password length</li>
                            <li>Minimum password length audit</li>
                            <li>Password must meet complexity requirements</li>
                            <li>Store password using reversible encryption</li>
                        </ul>
                    </div>
                    <div>
                        <p class="texts">By default, Active Directory Password Policies
                         do not include a solution to implement breached password protection.</p>
                         <p class="texts">Why is it critical for organizations to start thinking about password security breaches? 
                         Let's take a look at some best practice advice from prominent cybersecurity experts.</p>
                    </div>
                    <div>
                        <h2 class="texts h2-title"><b>New password policy recommendations</b></h2>
                        <p class="texts">As mentioned earlier, typical Active Directory password policies have limited 
                        features and capabilities. These allow you to create basic password policies that include length, 
                        complexity, age, and other criteria. However, there is no way to create broken password security using native technology.</p>
                        <p class="texts">While it is possible to use an <a href="https://docs.microsoft.com/en-us/windows/win32/secmgmt/installing-and-registering-a-password-filter-dll">Directory password filter .dll </a> to provide password dictionary protection, 
                        this is a manual procedure that requires the creation of specific password filter.dll files.</p>
                        <p class="texts">Leading cybersecurity experts, including as the National Institute of Standards and 
                        Technology (NIST), have issued new password policy guidelines that recommends broken password protection. 
                        The following is taken from NIST Special Publication 800-63B SP 800-63B Section 5.1.1.2 paragraph 9:</p>
                        <i class="texts lists">"Verifiers SHOULD NOT impose other composition rules (e.g., requiring mixtures of 
                        different character types or prohibiting consecutively repeated characters) for memorized secrets. 
                        Verifiers SHOULD NOT require memorized secrets to be changed arbitrarily (e.g., periodically). 
                        However, verifiers SHALL force a change if there is evidence of compromise of the authenticator."</i>
                    </div>
                    <div>
                        <p class="texts">In general, NIST advises that if there is evidence of a breach, businesses should 
                        require a password change. Businesses must be able to monitor the password landscape for leaked credentials 
                        in order to have proof of a password breach. In addition to keeping an eye on passwords that have been compromised, 
                        users' new password choices must be double-checked.</p>
                        <p class="texts">Basically, NIST's guidance recommends that organizations should force a password change 
                        if there is evidence of a breach. For businesses to have proof of a password breach, they must have a way 
                        to monitor the password landscape for breached passwords. In addition to monitoring for passwords to 
                        become breached, as users choose new passwords, the new password choices need to be checked.</p>
                    </div>
