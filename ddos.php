                        <h1 >New DDoS extortion attacks detected as Fancy Lazarus group returns</h1>
                    </div>
                    <div>
                        <p class="texts">Fancy Lazarus, a well-known threat group, reappears with a new ransom DDoS campaign aimed against a variety of industries.</p>
                    </div>
                    <img src="https://images.idgesg.net/images/article/2019/09/cio_cw_ddos_attacks_by_2alexd_gettyimages-1132952409_2400x1600-100811289-large.jpg" class="img">
                    <div>
                        <p class="texts">Threat actor organization Fancy Lazarus is engaging in new DDoS extortion behavior, according to security researchers. 
                        The attacks have largely targeted firms in the energy, finance, insurance, manufacturing, public utilities, and retail sectors in the 
                        United States and around the world.</p>
                    </div>
                    <div>
                        <p class="texts">Following a campaign of ransom DDoS attacks against global financial institutions and organizations that began in 
                        mid-to-late August 2020, the group—which previously went by the names Fancy Bear, Lazarus, Lazarus Group, and Armada Collective,
                        among others—went on hiatus for about a month from April to May 2021.</p>
                    </div>
                   <div>
                       <p class="texts">In each case, the threat actor requested bitcoin payment or else a small-scale denial-of-service assault would 
                        be launched, followed by a more serious strike just days later, according to Proofpoint experts. The gang has resurrected under
                        a new identity and with new tactics, techniques, and processes (TTPs).
                       </p>
                   </div>
                   <div>
                       <h2 class="texts h2-title">Fancy Lazarus' DDoS assault tactic has changed</h2>
                       <p class="texts">According to the experts, these fluctuations reflect the group's concerted endeavor to evolve its operations. 
                        The ransom payment has been cut from 10 bitcoin to a beginning price of 2 bitcoin (because to bitcoin's fluctuating value), 
                        and the content in the emails sent to recipients has been changed.</p>
                        <p class="texts">“The same information is sent in three email variants sent to the same recipients, but the email body is in
                       plain text, HTML, or as a JPG picture attachment. This is most likely an attempt to avoid discovery, according to the experts.</p>
                       <p class="texts">Previously, the sender would occasionally include the name of the targeted company's highest-ranking official,
                        such as the CEO. According to analysts, the most recent campaign uses a random first name, last name format, and the names appear to be made up.</p>
                        <p class="texts">“It's fascinating that the group is continually revising the initial email, which could indicate that it's working. 
                        They have, however, tested completely different wording in the emails between August 2020 and now,” researchers stated.</p>
                   </div>
                   <div>
                       <h2 class="texts h2-title">The growth of ransom DDoS attacks</h2>
                       <p class="texts">Despite ransom DDoS attacks are not new, Sherrod DeGrippo, senior director of Proofpoint's threat research and detection,
                        tells CSO that the increased acceptance of cryptocurrencies is causing a major increase in ransom DDoS attacks.</p>
                        <p class="texts">“Recently, there has been an increase in ransom DDoS activity, which began last year with activities from this gang. 
                        Since August 2020, when we first started tracking this activity, Proofpoint researchers have seen around 180 customers
                        send similar extortion emails from a variety of different and unconnected verticals. In the first month, about 59 of them were seen.”</p>
                       <p class="texts">DeGrippo says that ransom DDoS assaults are becoming more effective, especially against enterprises with web application
                        firewalls or upstream service providers capable of successfully filtering DDoS traffic from legitimate traffic.
                        She continues, "Threat actors are constantly seeking for the most efficient manner of attaining what they seek, in this case a cash payoff."
                        “DDoS assaults are becoming increasingly easy to launch, with a potentially large reward for far less effort than a ransomware campaign. Furthermore,
                        the threat actor avoids automated security safeguards that would flag and prohibit ransomware by launching this type of attack.”</p>
                        <p class="texts">DeGrippo concedes that it's difficult to know for sure whether the group's claims that their attack will peak at over 2 Tbps are true without complete visibility into the attacks.
                        “Some attacks have reportedly reached roughly 2 Tbps,” she claims, citing FBI reports and information sharing groups.</p>
                        <p class="texts">Regardless, DeGrippo advises that businesses should be prepared for such attacks by implementing suitable mitigations.
                        “Using a DoS protection service and having disaster recovery plans on hand are examples of this. When a DDoS attack occurs, a good response is dependent
                        on good technology and partnerships to help filter DDoS traffic. Organizations must have a strategy in place for dealing with these situations.</p>
                   </div>
