                        <h1>Social Engineering Scam Asks for Bitcoin Wallet Recovery Codes</h1>
                    </div>
                    <div>
                        <p class="texts">Twitter users having a hard time with their bitcoin wallets should be wary of accounts that offer to fix them. 
                        Attackers are using this social engineering method to trick cryptocurrency owners into forking over their wallet recovery codes.</p>
                        <p class="texts">Malwarebytes spotted multiple Twitter accounts seeking to take advantage of people searching for a bitcoin wallet recovery tool. The security firm described those efforts as ‘low maintenance’. 
                        That means all attackers needed to do was to set up a profile. Then, they could tweet out a link to a phishing landing page and wait.</p>
                    </div>
                    <img src="https://www.cisltd.com/app/uploads/2020/09/AdobeStock_191967596-1024x512.jpeg" class="img">
                    <div>
                        <p class="texts">Read on to learn what to watch out for when it comes to this type of social engineering.</p>
                        <h2 class="texts h2-title">Breaking the First Rule of Crypto</h2>
                        <p class="texts">In its analysis of the campaign, Malwarebytes found that digital attackers targeted Trust Wallet, 
                        an app that enables users to send, receive and store bitcoin, as well as other cryptocurrencies.</p>
                        <p class="texts">The attackers targeted real customer support threads on Twitter to trick users into clicking on a link. 
                        As part of the social engineering, another attack profile claimed the fake customer support team solved their problem.</p>
                        <p class="texts">But that link didn’t direct anyone to customer support. Instead, it sent them to a phishing landing page that 
                        asked them to describe their issue. It then asked users to submit their recovery phrase for their account.</p>
                        <p class="texts">That's a bad idea.</p>
                        <p class="texts">In late April 2021, the official Twitter account for this application warned users to always remember the “first 
                        rule of crypto”, that is,to never give out their recovery phrase. This is exactly the kind of rule social engineering attacks attempt to get around. 
                        This recovery code, which can consist of up to 12 words, is how users regain their accounts and their stored cryptocurrency if 
                        they lose access. In the wrong hands, the recovery phrase could enable attackers to drain their victims’ accounts.</p>
                        <p class="texts">Other profiles involved in this campaign auto-responded to tweets seeking help from the official account. In their responses, 
                        those profiles spammed out links to fake forms hosted on Google Docs. Of course, these also sought to steal users’ recovery phrases.</p>
                    </div>
                    <div>
                        <h2 class="texts h2-title">Other Twitter Social Engineering Scams</h2>
                        <p class="texts">Twitter phishing in general and customer support DM slide scams, in particular, have been used numerous times in the past few years.</p>
                        <p class="texts">Digital attackers used the same social engineering technique in April 2014. In that specific attack, they posed as customer support representatives for EA Sports on Twitter. They lead users to a fake website designed 
                        to steal access to EA Sports games. Attackers used the same social phishing tactics in 2016 to go after Natwest customers’ bank logins.</p>
                    </div>
                    <div>
                        <h2 class="texts h2-title">How to Defend Against Twitter Social Engineering </h2>
                        <p class="texts">Organizations can defend their employees against the types of Twitter social engineering discussed above by investing in their security awareness training programs. They can specifically use phishing simulations that emphasize 
                        how unlikely it is that official companies will ever use a form hosted on Google Docs to process official customer support requests. 
                        In addition, regularly remind users not to give out their passwords or other secrets to anyone.</p>
                      
